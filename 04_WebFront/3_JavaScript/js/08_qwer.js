
document.addEventListener("keyup", function(e){

   if(e.key == "q") {
      document.getElementById("inputQ").style.backgroundColor = "aqua";
   }

   if(e.key == "w") {
      document.getElementById("inputW").style.backgroundColor = "black";
   }   
   if(e.key == "e") {
      document.getElementById("inputE").style.backgroundColor = "black";
   }   
   if(e.key == "r") {
      document.getElementById("inputr").style.backgroundColor = "black";
   }   
});
   